package co.edureka.lang;

public class MathsAPI {

	public static void main(String[] args) {
		
		System.out.println(">> Pi is: "+Math.PI);
		System.out.println(">> E is: "+Math.E);
		System.out.println(">> Squareroot of 16 : "+Math.sqrt(16));
		System.out.println(">> Floor of 2.4 is: "+Math.floor(2.4));
		System.out.println(">> Log 100 is: "+Math.log10(100));
		
	}

}
